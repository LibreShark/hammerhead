    private readonly u32[] _seeds = new u32[4];
    private readonly bool _isV3;
    private u32 _newSeed;

    public GbaGsCrypter(bool isV3)
    {
        _isV3 = isV3;
    }

    public void DecryptCode(ref u32 address, ref u32 value)
    {
        _newSeed = 0;
        DeadFace(0);

        u32 rollingSeed = 0xC6EF3720;

        for (u8 i = 0; i < 32; i++)
        {
            u32 s0 = _seeds[0];
            u32 s1 = _seeds[1];
            u32 s2 = _seeds[2];
            u32 s3 = _seeds[3];

            u32 v1 = address << 4;
            u32 v2 = v1 + s2;
            u32 v3 = address + rollingSeed;
            u32 v4 = v2 ^ v3;
            u32 v5 = address >> 5;
            u32 v6 = v5 + s3;
            u32 v7 = v4 ^ v6;
            value -= v7;

            u32 a1 = value << 4;
            u32 a2 = a1 + s0;
            u32 a3 = value + rollingSeed;
            u32 a4 = a2 ^ a3;
            u32 a5 = value >> 5;
            u32 a6 = a5 + s1;
            u32 a7 = a4 ^ a6;
            address -= a7;

            rollingSeed -= 0x9E3779B9;
        }

        if (address == 0xDEADFACE)
        {
            DeadFace((u16)value);
        }
    }

    public void EncryptCode(ref u32 address, ref u32 value)
    {
        _newSeed = 0;
        DeadFace(0);

        u32 rollingSeed = 0;
        u32 oldAddr = address;
        u32 oldVal = value;

        for (u8 i = 0; i < 32; i++)
        {
            rollingSeed += 0x9E3779B9;
            address += (((value << 4) + _seeds[0]) ^ (value + rollingSeed)) ^ ((value >> 5) + _seeds[1]);
            value += (((address << 4) + _seeds[2]) ^ (address + rollingSeed)) ^ ((address >> 5) + _seeds[3]);
        }

        if (oldAddr == 0xDEADFACE)
        {
            DeadFace((u16)oldVal);
        }
    }

    private void DeadFace(u16 value)
    {
        u8[] deadTable1;
        u8[] deadTable2;

        if (_isV3)
        {
            deadTable1 = V3DeadTable1;
            deadTable2 = V3DeadTable2;
        }
        else
        {
            deadTable1 = V1DeadTable1;
            deadTable2 = V1DeadTable2;
        }

        for (u8 i = 0; i < 4; i++)
        {
            u8 df1 = (u8)(value & 0xFF00);
            u8 df2 = (u8)(df1 >> 8);
            u8 df3 = (u8)(value & 0xFF);
            u8 df4 = (u8)(df3 + i);
            _seeds[i] = SeedGen(df2, df4, deadTable1, deadTable2);
        }
    }

    private u32 SeedGen(u8 upper, u8 seed, u8[] deadtable1, u8[] deadtable2)
    {
        for (u8 i = 0; i < 4; i++)
        {
            u32 ns1 = _newSeed << 8;
            u8 ns2 = (u8)(i + upper);
            s32 ns3 = ns2 & 0xFF;
            u8 ns4 = deadtable1[ns3];
            u8 ns5 = (u8)(ns4 + deadtable2[seed]);
            s32 ns6 = ns5 & 0xFF;
            u32 ns7 = ns1 | (u32)ns6;
            _newSeed = ns7;
        }
        return _newSeed;
    }
